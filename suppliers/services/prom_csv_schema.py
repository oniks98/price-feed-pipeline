"""
PROM CSV schema - єдине джерело правди для структури CSV файлів
"""
from typing import List


class PromCsvSchema:
    """
    Схема CSV для PROM.ua
    
    Містить точні назви полів які очікує PROM (з дужками, комами, тощо).
    Використовується для генерації заголовків CSV.
    """
    
    # Базові поля для PROM CSV (ЄДИНЕ джерело правди)
    BASE_FIELDS: List[str] = [
        "Код_товару",
        "Назва_позиції",
        "Назва_позиції_укр",
        "Пошукові_запити",
        "Пошукові_запити_укр",
        "Опис",
        "Опис_укр",
        "Тип_товару",
        "Ціна",
        "Валюта",
        "Одиниця_виміру",
        "Мінімальний_обсяг_замовлення",
        "Оптова_ціна",
        "Мінімальне_замовлення_опт",
        "Посилання_зображення",
        "Наявність",
        "Кількість",
        "Номер_групи",
        "Назва_групи",
        "Посилання_підрозділу",
        "Можливість_поставки",
        "Термін_поставки",
        "Спосіб_пакування",
        "Спосіб_пакування_укр",
        "Унікальний_ідентифікатор",
        "Ідентифікатор_товару",
        "Ідентифікатор_підрозділу",
        "Ідентифікатор_групи",
        "Виробник",
        "Країна_виробник",
        "Знижка",
        "ID_групи_різновидів",
        "Особисті_нотатки",
        "Продукт_на_сайті",
        "Термін_дії_знижки_від",
        "Термін_дії_знижки_до",
        "Ціна_від",
        "Ярлик",
        "HTML_заголовок",
        "HTML_заголовок_укр",
        "HTML_опис",
        "HTML_опис_укр",
        "Код_маркування_(GTIN)",
        "Номер_пристрою_(MPN)",
        "Вага,кг",
        "Ширина,см",
        "Висота,см",
        "Довжина,см",
        "Де_знаходиться_товар",
    ]
    
    # Мапінг з Item полів (з underscores) на PROM поля (з дужками/комами)
    ITEM_TO_PROM_MAPPING = {
        "Код_маркування_GTIN": "Код_маркування_(GTIN)",
        "Номер_пристрою_MPN": "Номер_пристрою_(MPN)",
        "Вага_кг": "Вага,кг",
        "Ширина_см": "Ширина,см",
        "Висота_см": "Висота,см",
        "Довжина_см": "Довжина,см",
    }
    
    @classmethod
    def normalize_field_name(cls, item_field: str) -> str:
        """
        Конвертує назву поля з Item у формат PROM CSV
        
        Args:
            item_field: Назва поля в Item (наприклад, "Вага_кг")
        
        Returns:
            Назва поля для PROM CSV (наприклад, "Вага,кг")
        
        Examples:
            >>> PromCsvSchema.normalize_field_name("Вага_кг")
            "Вага,кг"
            >>> PromCsvSchema.normalize_field_name("Ціна")
            "Ціна"
        """
        return cls.ITEM_TO_PROM_MAPPING.get(item_field, item_field)
    
    @classmethod
    def get_header(cls, specs_limit: int = 160) -> List[str]:
        """
        Генерує повний заголовок CSV з базовими полями + характеристиками
        
        Args:
            specs_limit: Кількість характеристик (за замовчуванням 160)
        
        Returns:
            Список назв полів для CSV заголовка
        """
        header = cls.BASE_FIELDS[:]
        
        for _ in range(specs_limit):
            header.extend([
                "Назва_Характеристики",
                "Одиниця_виміру_Характеристики",
                "Значення_Характеристики",
            ])
        
        return header
    
    @classmethod
    def get_base_fields_count(cls) -> int:
        """Повертає кількість базових полів"""
        return len(cls.BASE_FIELDS)
